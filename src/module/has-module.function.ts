/**
 * Test for module referenced by specified key.
 *
 * @param key Module key.
 * @returns True when module exists, false otherwise.
 * @public
 * @static
 */
export function hasModule(key: string) {
  return isRootModuleBooted() && !!(window[rootModuleName] as Record<string, unknown>)[key];
}

