/**
 * Boot the root module.
 *
 * @returns True when successful or already booted, false otherwise.
 * @public
 * @static
 */
export function bootRootModule() {
  if (isRootModuleBooted()) {
    return true;
  }

  if (!window) {
    return false;
  }

  window[rootModuleName] = {};

  return true;
}

